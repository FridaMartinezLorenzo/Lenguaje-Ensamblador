;programa QUE IMPRIME UN CARACTER cON ATRIBUTO SERV 09 INT 10H
; Definicion de stack

.MODEL small
.STACK  100

;DEFINICION DE AREAS DE TRABAJO
.DATA
MSG1 DB "CARACTER A DESPLEGAR :$"
MSG2 DB 13, 10, " $"
ATRIBUTO DB ?
NUMERO DW ?

.CODE
PRINCI PROC FAR
   ;PROTOCOLO
   push ds
   sub ax,ax
   push ax
   MOV AX,@DATA
   MOV DS,AX
  

   ;INICIA PROGRAMA

LEA DX, MSG1
CALL MENSAJE
CALL LEE
LEA DX, MSG2
CALL MENSAJE
MOV ATRIBUTO,61H
MOV NUMERO, 60
CALL DESPLIEGA9
CALL LEE 

    ret

PRINCI ENDP


Despliega9 PROC
PUSH BX
PUSH CX
MOV AH,09 ; RECORDAR QUE EL CARACTER ESTA ALMACENADO EN AL
MOV BH,00
MOV BL, ATRIBUTO
MOV CX, NUMERO
INT 10H
POP CX
POP BX
RET
Despliega9 ENDP

MENSAJE PROC
PUSH AX
MOV AH,09H
INT 21H
POP AX
RET
MENSAJE ENDP

LEE PROC
MOV AH,01
INT 21H
RET
LEE ENDP

END PRINCI