;PROGRAMA QUE DIBUJA Y MUEVE UN PUNTO UTILIZANDO INT 21 SERV 07
; Definicion de stack
.MODEL small
.STACK  100

;DEFINICION DE AREAS DE TRABAJO
.DATA
;variables. 
 MEN  DB "HOLA.......$"

.CODE
PRINCI PROC FAR
   ;PROTOCOLO
   push ds
   sub ax,ax
   push ax
   MOV AX,@DATA
   MOV DS,AX
  

   ;INICIA PROGRAMA
   CALL LIMPIA
   CALL PREINI                             
                             



RET

PRINCI ENDP

limpia PROC NEAR
PUSH AX
PUSH BX
PUSH CX
PUSH DX
MOV AX,0600h
MOV BH,95H   ; FONDO 
MOV CX,0000H
MOV DX,184FH
INT 10h
POP DX
POP CX
POP BX
POP AX
RET
limpia ENDP

PREINI PROC NEAR
    MOV AH,10H
    INT 16H
    CMP AL,00H
    JE RASTREA
    CMP AL,050H
    JE RASTREA
    JMP SALI
  RASTREA:
    CMP AH,86H
    JNE SALI
    MOV AH,02
    MOV BH,00
    MOV DX,0C27H
    INT 10H  ;POSICIONAMIENTO DE CURSOR
    LEA DX,MEN
    MOV AH,09;ESCRIBE CADENA
    INT 21H
  SALI: RET
PREINI ENDP

END PRINCI
  
